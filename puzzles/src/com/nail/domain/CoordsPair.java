package com.nail.domain;

/**
 *
 * @author nail
 */
public class CoordsPair {
  private final int i;
  private final int j;

  public CoordsPair(int i, int j) {
    this.i = i;
    this.j = j;
  }

  public int getI() { return i; }
  public int getJ() { return j; }
}